# Para comenzar a trabajar en python, 1ro se abre el cmd.
# 
# 2do hay que crear o abrir el entorno virtual, en el cmd, con el siguiente comando:  
# crear:    python -m venv env
# abrir/activar:    .\env\Scripts\activate
# 
# 3ro Se pullea el último cambio realizado en el repositorio de git hub con:
# git init
# git pull
# Allí ya se descargaron los cambios realizados en la jornada anterior y se puede
# volver a trabajar.
# Los commit tambien se pueden realizar directamente desde vsc, en la sección
# correspondiente.
# 
# 4to Realizar git add . y al último el commit para poder ir guardando los cambios
# realizados.

# 5to desactivar el env con:
# deactivate

